#include "point.h"

// ----------------------------------------------------------------------------
// constructors
// ----------------------------------------------------------------------------
/******************************************************************************
 default consructor
 *****************************************************************************/
Point::Point()
{
    x = 0.0f;
    y = 0.0f;
}

/******************************************************************************
 property consructor
******************************************************************************/
Point::Point(float x, float y)
{
    this->x = x;
    this->y = y;
}

/******************************************************************************
 copy consructor
******************************************************************************/
Point::Point(Point* point)
{
    x = point->x;
    y = point->y;
}

// ----------------------------------------------------------------------------
// accessors (getters and setters)
// ----------------------------------------------------------------------------
float   Point::getX() { return x; }
void    Point::setX(float value) { x = value; }
float   Point::getY() { return y; }
void    Point::setY(float value) { y = value; }

/******************************************************************************
determines if the x and y values are  zero
returns:
    bool   - returns true if both are 0, false if either is non-zero
*******************************************************************************/
bool Point::IsEmpty()
{
    return !(x || y); // true if neither has a value
   
}
